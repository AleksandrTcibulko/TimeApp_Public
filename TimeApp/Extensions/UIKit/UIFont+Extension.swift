import UIKit

extension UIFont {
	static func verdana16() -> UIFont? {
		return UIFont(name: "Verdana", size: 16)
	}

	static func verdana15() -> UIFont? {
		return UIFont(name: "Verdana", size: 15)
	}

	static func verdana14() -> UIFont? {
		return UIFont(name: "Verdana", size: 14)
	}
}
